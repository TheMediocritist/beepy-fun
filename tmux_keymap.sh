function main_menu() {

	map=("   Ctrl  │  Tmux │   Meta    │  ESC │    Off   "
		 "   ──────┴───────┴───────────┴──────┴───────   "
		 "   #  ~│1 \`│2 {│3 }│( [│) ]│_ <│- >│+ ^│@  %   "
		 "    Q  │ W │ E │ R │ T │ Y │ U │ I │ O │ P     "
		 "   ────┼───┼───┼───┼───┼───┼───┼───┼───┼────   "
		 "   *  =│4 ÷│5 ±│6 ⋅│/ \\│: |│; &│'\'' “│"\"" ”│       "
		 "    A  │ S │ D │ F │ G │ H │ J │ K │ L │ BK    "
		 "   ────┼───┼───┼───┼───┼───┼───┼───┼───┼────   "
		 "       │7 ¥│8 €│9 £│? ¿│! ¡│, <│. >│\`  │       "
		 "   LALT│ Z │ X │ C │ V │ B │ N │ M │ $ │ ENT   "
		 "   ────┴───┴───┴───┴───┴───┴───┴───┴───┴────   "
		 "     │     │0    │TAB        │     │     │     "
		 "     │ LSH │  ~  │   SPACE   │ SYM │ RSH │    "
	)
	map=${map[@]}
	cmd=("tmux" "display-popup" "-h" "15" "-w" "50" "echo -n '$map'")
	"${cmd[@]}"
}

main_menu
